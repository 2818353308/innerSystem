package cn.baisee.utils.vo;

public class Message {
	
	private String context;

	
	
	
	public Message() {
		super();
	}

	public Message(String context) {
		super();
		this.context = context;
	}

	public String getContext() {
		return context;
	}

	public void setContext(String context) {
		this.context = context;
	}
	
	

}
